// Code generator: reads content-types/*.json → produces .graphql schema files,
// resolver implementations, and gqlgen.yml model mappings.
package main

import (
	"flag"
	"fmt"
	"log"
	"os"
	"path/filepath"
	"strings"

	"project-abyssoftime-cms-v2/api/internal/domain/entity"
	contenttype "project-abyssoftime-cms-v2/api/internal/usecase/content_type"
)

func main() {
	phase := flag.String("phase", "all", "schema | resolvers | all")
	flag.Parse()

	rootDir, err := findRootDir()
	if err != nil {
		log.Fatalf("find root dir: %v", err)
	}

	contentTypesDir := filepath.Join(rootDir, "content-types")
	graphqlDir := filepath.Join(rootDir, "graphql")

	defs, err := contenttype.LoadDefinitions(contentTypesDir)
	if err != nil {
		log.Fatalf("load content-type definitions: %v", err)
	}

	switch *phase {
	case "schema":
		if err := generateSchema(graphqlDir, defs); err != nil {
			log.Fatalf("generate schema: %v", err)
		}
		if err := injectModels(graphqlDir, defs); err != nil {
			log.Fatalf("inject models: %v", err)
		}
	case "resolvers":
		if err := generateResolvers(graphqlDir, defs); err != nil {
			log.Fatalf("generate resolvers: %v", err)
		}
	case "all":
		if err := generateSchema(graphqlDir, defs); err != nil {
			log.Fatalf("generate schema: %v", err)
		}
		if err := injectModels(graphqlDir, defs); err != nil {
			log.Fatalf("inject models: %v", err)
		}
		if err := generateResolvers(graphqlDir, defs); err != nil {
			log.Fatalf("generate resolvers: %v", err)
		}
	default:
		log.Fatalf("unknown phase: %s (expected schema, resolvers, or all)", *phase)
	}

	log.Printf("gqlcodegen: phase=%s completed for %d content types", *phase, len(defs))
}

func findRootDir() (string, error) {
	dir, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("go.mod not found")
		}
		dir = parent
	}
}

// ── Schema Generation ──

func generateSchema(graphqlDir string, defs []contenttype.ContentTypeDefinition) error {
	schemaDir := filepath.Join(graphqlDir, "schema")
	if err := os.MkdirAll(schemaDir, 0o755); err != nil {
		return err
	}

	if err := os.WriteFile(filepath.Join(schemaDir, "base.graphql"), []byte(buildBaseSchema()), 0o644); err != nil {
		return fmt.Errorf("write base.graphql: %w", err)
	}

	for _, def := range defs {
		sdl := buildContentTypeSDL(def)
		filename := def.Slug + ".graphql"
		if err := os.WriteFile(filepath.Join(schemaDir, filename), []byte(sdl), 0o644); err != nil {
			return fmt.Errorf("write %s: %w", filename, err)
		}
	}

	return nil
}

func buildBaseSchema() string {
	return `# Code generated by gqlcodegen. DO NOT EDIT.

directive @auth on FIELD_DEFINITION

scalar JSON
scalar Time

enum SortOrder {
  ASC
  DESC
}

input IDFilter {
  eq: ID
  ne: ID
  in: [ID!]
  notIn: [ID!]
}

input StringFilter {
  eq: String
  ne: String
  in: [String!]
  notIn: [String!]
}

input NumberFilter {
  eq: Float
  ne: Float
  in: [Float!]
  notIn: [Float!]
}

input BooleanFilter {
  eq: Boolean
  ne: Boolean
}

input TimeFilter {
  eq: Time
  ne: Time
}

type MediaAsset {
  documentId: ID!
  url: String!
  thumbnailUrl: String
  fileName: String
  width: Int
  height: Int
}

type ContentType {
  id: ID!
  name: String!
  slug: String!
  kind: String!
  createdAt: Time!
  updatedAt: Time!
}

type Query {
  contentTypes: [ContentType!]!
}

type Mutation {
  _empty: Boolean
}
`
}

func buildContentTypeSDL(def contenttype.ContentTypeDefinition) string {
	typeName := slugToPascalCase(def.Slug)
	camel := slugToCamelCase(def.Slug)

	var builder strings.Builder
	builder.WriteString("# Code generated by gqlcodegen. DO NOT EDIT.\n\n")

	for _, field := range def.Fields {
		if field.Type == "component" {
			writeComponentType(&builder, typeName, field)
		}
	}

	fmt.Fprintf(&builder, "type %s {\n", typeName)
	builder.WriteString("  documentId: ID!\n")
	for _, field := range def.Fields {
		if field.Type == "component" {
			compType := typeName + slugToPascalCase(field.Name)
			if field.Repeatable {
				fmt.Fprintf(&builder, "  %s: [%s!]\n", field.Name, compType)
			} else {
				fmt.Fprintf(&builder, "  %s: %s\n", field.Name, compType)
			}
			continue
		}
		fmt.Fprintf(&builder, "  %s: %s\n", field.Name, fieldTypeToGraphQL(field.Type))
	}
	builder.WriteString("  locale: String!\n")
	builder.WriteString("  createdAt: Time!\n")
	builder.WriteString("  updatedAt: Time!\n")
	builder.WriteString("  publishedAt: Time\n")
	builder.WriteString("}\n\n")

	fmt.Fprintf(&builder, "input %sInput {\n", typeName)
	for _, field := range def.Fields {
		if field.Type == "component" {
			continue
		}
		fmt.Fprintf(&builder, "  %s: %s\n", field.Name, fieldTypeToInputGraphQL(field.Type))
	}
	builder.WriteString("}\n\n")

	if def.Kind == "collection" {
		writeFilterType(&builder, typeName, def.Fields)
		writeOrderByType(&builder, typeName, def.Fields)
	}

	if def.Kind == "collection" {
		builder.WriteString("extend type Query {\n")
		fmt.Fprintf(&builder, "  %s(documentId: ID!, locale: String, status: String): %s\n", camel, typeName)
		fmt.Fprintf(&builder, "  %s(filters: [%sFilter!], orderBy: %sOrderBy, start: Int, size: Int, locale: String, status: String): [%s!]!\n", pluralize(camel), typeName, typeName, typeName)
		builder.WriteString("}\n\n")

		builder.WriteString("extend type Mutation {\n")
		fmt.Fprintf(&builder, "  create%s(data: %sInput!): %s! @auth\n", typeName, typeName, typeName)
		fmt.Fprintf(&builder, "  update%s(documentId: ID!, data: %sInput!): %s! @auth\n", typeName, typeName, typeName)
		fmt.Fprintf(&builder, "  delete%s(documentId: ID!): Boolean! @auth\n", typeName)
		fmt.Fprintf(&builder, "  publish%s(documentId: ID!, locale: String): %s! @auth\n", typeName, typeName)
		fmt.Fprintf(&builder, "  unpublish%s(documentId: ID!, locale: String): %s! @auth\n", typeName, typeName)
		builder.WriteString("}\n")
	} else {
		builder.WriteString("extend type Query {\n")
		fmt.Fprintf(&builder, "  %s(locale: String, status: String): %s\n", camel, typeName)
		builder.WriteString("}\n\n")

		builder.WriteString("extend type Mutation {\n")
		fmt.Fprintf(&builder, "  save%s(data: %sInput!, locale: String): %s! @auth\n", typeName, typeName, typeName)
		fmt.Fprintf(&builder, "  publish%s(locale: String): %s! @auth\n", typeName, typeName)
		fmt.Fprintf(&builder, "  unpublish%s(locale: String): %s! @auth\n", typeName, typeName)
		builder.WriteString("}\n")
	}

	return builder.String()
}

func writeComponentType(builder *strings.Builder, parentType string, field entity.FieldDefinition) {
	compType := parentType + slugToPascalCase(field.Name)

	for _, sub := range field.Fields {
		if sub.Type == "component" {
			writeComponentType(builder, compType, sub)
		}
	}

	fmt.Fprintf(builder, "type %s {\n", compType)
	for _, sub := range field.Fields {
		if sub.Type == "component" {
			nestedType := compType + slugToPascalCase(sub.Name)
			if sub.Repeatable {
				fmt.Fprintf(builder, "  %s: [%s!]\n", sub.Name, nestedType)
			} else {
				fmt.Fprintf(builder, "  %s: %s\n", sub.Name, nestedType)
			}
			continue
		}
		fmt.Fprintf(builder, "  %s: %s\n", sub.Name, fieldTypeToGraphQL(sub.Type))
	}
	builder.WriteString("}\n\n")
}

func writeFilterType(builder *strings.Builder, typeName string, fields []entity.FieldDefinition) {
	fmt.Fprintf(builder, "input %sFilter {\n", typeName)
	builder.WriteString("  documentId: IDFilter\n")
	builder.WriteString("  createdAt: TimeFilter\n")
	builder.WriteString("  updatedAt: TimeFilter\n")
	builder.WriteString("  publishedAt: TimeFilter\n")
	for _, field := range fields {
		switch field.Type {
		case "text", "richtext":
			fmt.Fprintf(builder, "  %s: StringFilter\n", field.Name)
		case "number":
			fmt.Fprintf(builder, "  %s: NumberFilter\n", field.Name)
		case "boolean":
			fmt.Fprintf(builder, "  %s: BooleanFilter\n", field.Name)
		}
	}
	fmt.Fprintf(builder, "  and: [%sFilter!]\n", typeName)
	fmt.Fprintf(builder, "  or: [%sFilter!]\n", typeName)
	fmt.Fprintf(builder, "  not: %sFilter\n", typeName)
	builder.WriteString("}\n\n")
}

func writeOrderByType(builder *strings.Builder, typeName string, fields []entity.FieldDefinition) {
	fmt.Fprintf(builder, "input %sOrderBy {\n", typeName)
	for _, field := range fields {
		switch field.Type {
		case "text", "richtext", "number", "boolean":
			fmt.Fprintf(builder, "  %s: SortOrder\n", field.Name)
		}
	}
	builder.WriteString("  createdAt: SortOrder\n")
	builder.WriteString("  updatedAt: SortOrder\n")
	builder.WriteString("  publishedAt: SortOrder\n")
	builder.WriteString("}\n\n")
}

// ── Resolver Generation ──

func generateResolvers(graphqlDir string, defs []contenttype.ContentTypeDefinition) error {
	resolverDir := filepath.Join(graphqlDir, "resolver")
	if err := os.MkdirAll(resolverDir, 0o755); err != nil {
		return err
	}

	var builder strings.Builder
	builder.WriteString("// Code generated by gqlcodegen from content-types/*.json. DO NOT EDIT.\n")
	builder.WriteString("package resolver\n\n")
	builder.WriteString("import (\n")
	builder.WriteString("\t\"context\"\n")
	builder.WriteString("\t\"time\"\n\n")
	builder.WriteString("\t\"project-abyssoftime-cms-v2/api/graphql/generated\"\n")
	builder.WriteString("\t\"project-abyssoftime-cms-v2/api/graphql/model\"\n")
	builder.WriteString("\t\"project-abyssoftime-cms-v2/api/internal/domain/entity\"\n")
	builder.WriteString(")\n\n")
	builder.WriteString("// Ensure unused imports are used.\n")
	builder.WriteString("var _ time.Time\n")
	builder.WriteString("var _ context.Context\n\n")

	writeResolverTypes(&builder)

	for _, def := range defs {
		writeFieldDefinitions(&builder, def)
		builder.WriteString("\n")

		if def.Kind == "collection" {
			writeCollectionResolvers(&builder, def)
		} else {
			writeSingleResolvers(&builder, def)
		}
		builder.WriteString("\n")
	}

	writeResolverRootMethods(&builder, defs)
	writeMapFieldResolvers(&builder, defs)

	outPath := filepath.Join(resolverDir, "content_gen.go")
	return os.WriteFile(outPath, []byte(builder.String()), 0o644)
}

func writeResolverTypes(builder *strings.Builder) {
	builder.WriteString("// ── Resolver types + root methods ──\n\n")
	builder.WriteString("type queryResolver struct{ *Resolver }\n")
	builder.WriteString("type mutationResolver struct{ *Resolver }\n\n")
	builder.WriteString("func (resolver *Resolver) Query() generated.QueryResolver       { return &queryResolver{resolver} }\n")
	builder.WriteString("func (resolver *Resolver) Mutation() generated.MutationResolver { return &mutationResolver{resolver} }\n\n")

	builder.WriteString("func (resolver *queryResolver) ContentTypes(ctx context.Context) ([]map[string]interface{}, error) {\n")
	builder.WriteString("\tcontentTypes, err := resolver.ctUC.FindAll(ctx)\n")
	builder.WriteString("\tif err != nil {\n\t\treturn nil, err\n\t}\n")
	builder.WriteString("\tresult := make([]map[string]interface{}, len(contentTypes))\n")
	builder.WriteString("\tfor idx, contentType := range contentTypes {\n")
	builder.WriteString("\t\tresult[idx] = map[string]interface{}{\n")
	builder.WriteString("\t\t\t\"id\": contentType.ID, \"name\": contentType.Name, \"slug\": contentType.Slug,\n")
	builder.WriteString("\t\t\t\"kind\": string(contentType.Kind), \"createdAt\": contentType.CreatedAt, \"updatedAt\": contentType.UpdatedAt,\n")
	builder.WriteString("\t\t}\n")
	builder.WriteString("\t}\n")
	builder.WriteString("\treturn result, nil\n")
	builder.WriteString("}\n\n")

	builder.WriteString("func (resolver *mutationResolver) Empty(_ context.Context) (*bool, error) {\n")
	builder.WriteString("\treturn nil, nil\n")
	builder.WriteString("}\n\n")
}

func writeFieldDefinitions(builder *strings.Builder, def contenttype.ContentTypeDefinition) {
	varName := slugToCamelCase(def.Slug) + "Fields"
	fmt.Fprintf(builder, "var %s = []entity.FieldDefinition{\n", varName)
	writeFieldSlice(builder, def.Fields, 1)
	builder.WriteString("}\n")
}

func writeFieldSlice(builder *strings.Builder, fields []entity.FieldDefinition, depth int) {
	indent := strings.Repeat("\t", depth)
	for _, field := range fields {
		fmt.Fprintf(builder, "%s{Name: %q, Type: %q", indent, field.Name, field.Type)
		if field.Repeatable {
			builder.WriteString(", Repeatable: true")
		}
		if len(field.Fields) > 0 {
			builder.WriteString(", Fields: []entity.FieldDefinition{\n")
			writeFieldSlice(builder, field.Fields, depth+1)
			fmt.Fprintf(builder, "%s}", indent)
		}
		builder.WriteString("},\n")
	}
}

func writeCollectionResolvers(builder *strings.Builder, def contenttype.ContentTypeDefinition) {
	pascal := slugToPascalCase(def.Slug)
	camel := slugToCamelCase(def.Slug)
	fieldsVar := camel + "Fields"

	fmt.Fprintf(builder, "func (resolver *queryResolver) %s(ctx context.Context, %sID string, locale *string, status *string) (map[string]interface{}, error) {\n", pascal, camel)
	fmt.Fprintf(builder, "\treturn resolver.getDocument(ctx, %q, %sID, locale, status, %s)\n", def.Slug, camel, fieldsVar)
	builder.WriteString("}\n\n")

	fmt.Fprintf(builder, "func (resolver *queryResolver) %s(ctx context.Context, filters []*model.%sFilter, orderBy *model.%sOrderBy, start *int, size *int, locale *string, status *string) ([]map[string]interface{}, error) {\n", pluralize(pascal), pascal, pascal)
	fmt.Fprintf(builder, "\treturn resolver.getDocumentList(ctx, %q, filters, orderBy, start, size, locale, status, %s)\n", def.Slug, fieldsVar)
	builder.WriteString("}\n\n")

	fmt.Fprintf(builder, "func (resolver *mutationResolver) Create%s(ctx context.Context, data model.%sInput) (map[string]interface{}, error) {\n", pascal, pascal)
	fmt.Fprintf(builder, "\treturn resolver.createDocument(ctx, %q, data, %s)\n", def.Slug, fieldsVar)
	builder.WriteString("}\n\n")

	fmt.Fprintf(builder, "func (resolver *mutationResolver) Update%s(ctx context.Context, %sID string, data model.%sInput) (map[string]interface{}, error) {\n", pascal, camel, pascal)
	fmt.Fprintf(builder, "\treturn resolver.updateDocument(ctx, %q, %sID, data, %s)\n", def.Slug, camel, fieldsVar)
	builder.WriteString("}\n\n")

	fmt.Fprintf(builder, "func (resolver *mutationResolver) Delete%s(ctx context.Context, %sID string) (bool, error) {\n", pascal, camel)
	fmt.Fprintf(builder, "\treturn resolver.deleteDocument(ctx, %q, %sID, %s)\n", def.Slug, camel, fieldsVar)
	builder.WriteString("}\n\n")

	fmt.Fprintf(builder, "func (resolver *mutationResolver) Publish%s(ctx context.Context, %sID string, locale *string) (map[string]interface{}, error) {\n", pascal, camel)
	fmt.Fprintf(builder, "\treturn resolver.publishDocument(ctx, %q, %sID, locale, %s)\n", def.Slug, camel, fieldsVar)
	builder.WriteString("}\n\n")

	fmt.Fprintf(builder, "func (resolver *mutationResolver) Unpublish%s(ctx context.Context, %sID string, locale *string) (map[string]interface{}, error) {\n", pascal, camel)
	fmt.Fprintf(builder, "\treturn resolver.unpublishDocument(ctx, %q, %sID, locale, %s)\n", def.Slug, camel, fieldsVar)
	builder.WriteString("}\n")
}

