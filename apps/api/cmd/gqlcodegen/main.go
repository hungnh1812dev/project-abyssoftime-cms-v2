// ── ResolverRoot + Map Field Resolvers ──

type mapField struct {
	Name     string
	GoType   string
	Nullable bool
	MapKey   string
}

func writeResolverRootMethods(builder *strings.Builder, defs []contenttype.ContentTypeDefinition) {
	builder.WriteString("// ── ResolverRoot methods ──\n\n")

	builder.WriteString("type mediaAssetFieldResolver struct{ *Resolver }\n")
	builder.WriteString("type contentTypeFieldResolver struct{ *Resolver }\n\n")

	builder.WriteString("func (resolver *Resolver) MediaAsset() generated.MediaAssetResolver { return &mediaAssetFieldResolver{resolver} }\n")
	builder.WriteString("func (resolver *Resolver) ContentType() generated.ContentTypeResolver { return &contentTypeFieldResolver{resolver} }\n\n")

	for _, def := range defs {
		pascal := slugToPascalCase(def.Slug)
		lower := slugToCamelCase(def.Slug)
		resolverType := lower + "FieldResolver"
		fmt.Fprintf(builder, "type %s struct{ *Resolver }\n", resolverType)
		fmt.Fprintf(builder, "func (resolver *Resolver) %s() generated.%sResolver { return &%s{resolver} }\n\n", pascal, pascal, resolverType)

		writeComponentResolverRootMethods(builder, pascal, def.Fields)
	}
}

func writeComponentResolverRootMethods(builder *strings.Builder, parentType string, fields []entity.FieldDefinition) {
	for _, field := range fields {
		if field.Type != "component" {
			continue
		}
		compType := parentType + slugToPascalCase(field.Name)
		lower := toLowerFirst(compType)
		resolverType := lower + "FieldResolver"
		fmt.Fprintf(builder, "type %s struct{ *Resolver }\n", resolverType)
		fmt.Fprintf(builder, "func (resolver *Resolver) %s() generated.%sResolver { return &%s{resolver} }\n\n", compType, compType, resolverType)

		writeComponentResolverRootMethods(builder, compType, field.Fields)
	}
}

func writeMapFieldResolvers(builder *strings.Builder, defs []contenttype.ContentTypeDefinition) {
	builder.WriteString("// ── Map field resolvers ──\n\n")

	writeMapFieldResolverMethods(builder, "mediaAssetFieldResolver", []mapField{
		{Name: "DocumentID", GoType: "string", Nullable: false, MapKey: "documentId"},
		{Name: "URL", GoType: "string", Nullable: false, MapKey: "url"},
		{Name: "ThumbnailURL", GoType: "string", Nullable: true, MapKey: "thumbnailUrl"},
		{Name: "FileName", GoType: "string", Nullable: true, MapKey: "fileName"},
		{Name: "Width", GoType: "int", Nullable: true, MapKey: "width"},
		{Name: "Height", GoType: "int", Nullable: true, MapKey: "height"},
	})

	writeMapFieldResolverMethods(builder, "contentTypeFieldResolver", []mapField{
		{Name: "ID", GoType: "string", Nullable: false, MapKey: "id"},
		{Name: "Name", GoType: "string", Nullable: false, MapKey: "name"},
		{Name: "Slug", GoType: "string", Nullable: false, MapKey: "slug"},
		{Name: "Kind", GoType: "string", Nullable: false, MapKey: "kind"},
		{Name: "CreatedAt", GoType: "time.Time", Nullable: true, MapKey: "createdAt"},
		{Name: "UpdatedAt", GoType: "time.Time", Nullable: true, MapKey: "updatedAt"},
	})

	for _, def := range defs {
		pascal := slugToPascalCase(def.Slug)
		lower := slugToCamelCase(def.Slug)
		resolverType := lower + "FieldResolver"

		docFields := buildDocumentMapFields(def.Fields)
		writeMapFieldResolverMethods(builder, resolverType, docFields)

		writeComponentFieldResolvers(builder, pascal, def.Fields)
	}
}

func writeComponentFieldResolvers(builder *strings.Builder, parentType string, fields []entity.FieldDefinition) {
	for _, field := range fields {
		if field.Type != "component" {
			continue
		}
		compType := parentType + slugToPascalCase(field.Name)
		lower := toLowerFirst(compType)
		resolverType := lower + "FieldResolver"

		compFields := buildComponentMapFields(field.Fields)
		writeMapFieldResolverMethods(builder, resolverType, compFields)

		writeComponentFieldResolvers(builder, compType, field.Fields)
	}
}

func buildDocumentMapFields(fields []entity.FieldDefinition) []mapField {
	result := []mapField{
		{Name: "DocumentID", GoType: "string", Nullable: false, MapKey: "documentId"},
	}
	for _, field := range fields {
		result = append(result, fieldToMapField(field))
	}
	result = append(result,
		mapField{Name: "Locale", GoType: "string", Nullable: false, MapKey: "locale"},
		mapField{Name: "CreatedAt", GoType: "time.Time", Nullable: false, MapKey: "createdAt"},
		mapField{Name: "UpdatedAt", GoType: "time.Time", Nullable: false, MapKey: "updatedAt"},
		mapField{Name: "PublishedAt", GoType: "time.Time", Nullable: true, MapKey: "publishedAt"},
	)
	return result
}

func buildComponentMapFields(fields []entity.FieldDefinition) []mapField {
	var result []mapField
	for _, field := range fields {
		result = append(result, fieldToMapField(field))
	}
	return result
}

func fieldToMapField(field entity.FieldDefinition) mapField {
	name := slugToPascalCase(field.Name)
	switch field.Type {
	case "text", "richtext":
		return mapField{Name: name, GoType: "string", Nullable: true, MapKey: field.Name}
	case "number":
		return mapField{Name: name, GoType: "float64", Nullable: true, MapKey: field.Name}
	case "boolean":
		return mapField{Name: name, GoType: "bool", Nullable: true, MapKey: field.Name}
	case "media":
		return mapField{Name: name, GoType: "map[string]interface{}", Nullable: true, MapKey: field.Name}
	case "json":
		return mapField{Name: name, GoType: "map[string]interface{}", Nullable: true, MapKey: field.Name}
	case "component":
		if field.Repeatable {
			return mapField{Name: name, GoType: "[]map[string]interface{}", Nullable: true, MapKey: field.Name}
		}
		return mapField{Name: name, GoType: "map[string]interface{}", Nullable: true, MapKey: field.Name}
	default:
		return mapField{Name: name, GoType: "string", Nullable: true, MapKey: field.Name}
	}
}

func writeMapFieldResolverMethods(builder *strings.Builder, resolverType string, fields []mapField) {
	for _, field := range fields {
		writeMapFieldMethod(builder, resolverType, field)
	}
	builder.WriteString("\n")
}

func writeMapFieldMethod(builder *strings.Builder, resolverType string, field mapField) {
	goReturnType := resolveReturnType(field)

	fmt.Fprintf(builder, "func (resolver *%s) %s(_ context.Context, obj map[string]interface{}) (%s, error) {\n", resolverType, field.Name, goReturnType)

	writeFieldExtractor(builder, field)

	builder.WriteString("}\n\n")
}

func resolveReturnType(field mapField) string {
	switch field.GoType {
	case "time.Time":
		return "*time.Time"
	case "map[string]interface{}":
		return "map[string]interface{}"
	case "[]map[string]interface{}":
		return "[]map[string]interface{}"
	default:
		if field.Nullable {
			return "*" + field.GoType
		}
		return field.GoType
	}
}

func writeFieldExtractor(builder *strings.Builder, field mapField) {
	switch field.GoType {
	case "string":
		if field.Nullable {
			fmt.Fprintf(builder, "\tval, exists := obj[%q].(string)\n", field.MapKey)
			builder.WriteString("\tif !exists {\n\t\treturn nil, nil\n\t}\n")
			builder.WriteString("\treturn &val, nil\n")
		} else {
			fmt.Fprintf(builder, "\tval, _ := obj[%q].(string)\n", field.MapKey)
			builder.WriteString("\treturn val, nil\n")
		}
	case "float64":
		fmt.Fprintf(builder, "\tswitch val := obj[%q].(type) {\n", field.MapKey)
		builder.WriteString("\tcase float64:\n")
		builder.WriteString("\t\treturn &val, nil\n")
		builder.WriteString("\tcase int:\n")
		builder.WriteString("\t\tcasted := float64(val)\n")
		builder.WriteString("\t\treturn &casted, nil\n")
		builder.WriteString("\tdefault:\n")
		builder.WriteString("\t\treturn nil, nil\n")
		builder.WriteString("\t}\n")
	case "bool":
		fmt.Fprintf(builder, "\tval, exists := obj[%q].(bool)\n", field.MapKey)
		builder.WriteString("\tif !exists {\n\t\treturn nil, nil\n\t}\n")
		builder.WriteString("\treturn &val, nil\n")
	case "int":
		fmt.Fprintf(builder, "\tswitch val := obj[%q].(type) {\n", field.MapKey)
		builder.WriteString("\tcase int:\n")
		builder.WriteString("\t\treturn &val, nil\n")
		builder.WriteString("\tcase float64:\n")
		builder.WriteString("\t\tcasted := int(val)\n")
		builder.WriteString("\t\treturn &casted, nil\n")
		builder.WriteString("\tdefault:\n")
		builder.WriteString("\t\treturn nil, nil\n")
		builder.WriteString("\t}\n")
	case "time.Time":
		fmt.Fprintf(builder, "\tswitch val := obj[%q].(type) {\n", field.MapKey)
		builder.WriteString("\tcase time.Time:\n")
		builder.WriteString("\t\treturn &val, nil\n")
		builder.WriteString("\tcase *time.Time:\n")
		builder.WriteString("\t\treturn val, nil\n")
		builder.WriteString("\tdefault:\n")
		builder.WriteString("\t\treturn nil, nil\n")
		builder.WriteString("\t}\n")
	case "map[string]interface{}":
		fmt.Fprintf(builder, "\tval, _ := obj[%q].(map[string]interface{})\n", field.MapKey)
		builder.WriteString("\treturn val, nil\n")
	case "[]map[string]interface{}":
		fmt.Fprintf(builder, "\traw, _ := obj[%q].([]interface{})\n", field.MapKey)
		builder.WriteString("\tif raw == nil {\n\t\treturn nil, nil\n\t}\n")
		builder.WriteString("\tresult := make([]map[string]interface{}, 0, len(raw))\n")
		builder.WriteString("\tfor _, item := range raw {\n")
		builder.WriteString("\t\tif itemMap, isMap := item.(map[string]interface{}); isMap {\n")
		builder.WriteString("\t\t\tresult = append(result, itemMap)\n")
		builder.WriteString("\t\t}\n")
		builder.WriteString("\t}\n")
		builder.WriteString("\treturn result, nil\n")
	default:
		fmt.Fprintf(builder, "\tval, _ := obj[%q].(%s)\n", field.MapKey, field.GoType)
		if field.Nullable {
			builder.WriteString("\treturn &val, nil\n")
		} else {
			builder.WriteString("\treturn val, nil\n")
		}
	}
}

func toLowerFirst(name string) string {
	if len(name) == 0 {
		return name
	}
	return strings.ToLower(name[:1]) + name[1:]
}

// ── gqlgen.yml Model Injection ──

func injectModels(graphqlDir string, defs []contenttype.ContentTypeDefinition) error {
	ymlPath := filepath.Join(graphqlDir, "gqlgen.yml")
	data, err := os.ReadFile(ymlPath)
	if err != nil {
		return fmt.Errorf("read gqlgen.yml: %w", err)
	}

	content := string(data)
	marker := "# --- gqlcodegen models start ---"
	endMarker := "# --- gqlcodegen models end ---"

	if idx := strings.Index(content, marker); idx >= 0 {
		endIdx := strings.Index(content, endMarker)
		if endIdx >= 0 {
			content = strings.TrimRight(content[:idx], " \n") + "\n"
		}
	}

	content = strings.TrimRight(content, "\n") + "\n"

	var models strings.Builder
	models.WriteString("  " + marker + "\n")

	models.WriteString("  MediaAsset:\n")
	models.WriteString("    model: \"project-abyssoftime-cms-v2/api/graphql/model.MediaAssetMap\"\n")
	models.WriteString("  ContentType:\n")
	models.WriteString("    model: \"project-abyssoftime-cms-v2/api/graphql/model.ContentTypeMap\"\n")

	for _, def := range defs {
		pascal := slugToPascalCase(def.Slug)
		fmt.Fprintf(&models, "  %s:\n", pascal)
		models.WriteString("    model: \"project-abyssoftime-cms-v2/api/graphql/model.DocumentMap\"\n")

		collectComponentTypes(&models, pascal, def.Fields)
	}
	models.WriteString("  " + endMarker + "\n")

	content += models.String()

	return os.WriteFile(ymlPath, []byte(content), 0o644)
}

func collectComponentTypes(builder *strings.Builder, parentType string, fields []entity.FieldDefinition) {
	for _, field := range fields {
		if field.Type != "component" {
			continue
		}
		compType := parentType + slugToPascalCase(field.Name)
		fmt.Fprintf(builder, "  %s:\n", compType)
		builder.WriteString("    model: \"project-abyssoftime-cms-v2/api/graphql/model.DocumentMap\"\n")
		collectComponentTypes(builder, compType, field.Fields)
	}
}

// ── Naming helpers ──

func slugToPascalCase(slug string) string {
	parts := strings.Split(slug, "-")
	for idx, part := range parts {
		if len(part) > 0 {
			parts[idx] = strings.ToUpper(part[:1]) + part[1:]
		}
	}
	return strings.Join(parts, "")
}

func slugToCamelCase(slug string) string {
	pascal := slugToPascalCase(slug)
	if len(pascal) == 0 {
		return ""
	}
	return strings.ToLower(pascal[:1]) + pascal[1:]
}

func pluralize(name string) string {
	if len(name) == 0 {
		return name
	}
	lower := strings.ToLower(name)
	if strings.HasSuffix(lower, "s") || strings.HasSuffix(lower, "x") || strings.HasSuffix(lower, "z") || strings.HasSuffix(lower, "ch") || strings.HasSuffix(lower, "sh") {
		return name + "es"
	}
	return name + "s"
}

